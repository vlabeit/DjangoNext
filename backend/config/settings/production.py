# CORS / CSRF cross-origin
# ------------------------------------------------------------------------------
# With the bundled Traefik setup the browser talks to Next.js and Django under
# ONE origin (https://__DOMAIN__), so the API is same-origin: CORS never fires
# and same-origin POSTs already pass CSRF. You only need to touch these when an
# API client lives on a DIFFERENT origin — a separately-hosted frontend, a
# native/mobile app, or the www↔apex split with an absolute API URL.
#
# CSRF_TRUSTED_ORIGINS — full origins (WITH scheme) Django accepts for unsafe
# (POST/PUT/…) requests. Defaults to this project's own apex + www so the www↔
# apex case works without config; override via env to add more.
# https://docs.djangoproject.com/en/dev/ref/settings/#csrf-trusted-origins
CSRF_TRUSTED_ORIGINS = env.list(
    "DJANGO_CSRF_TRUSTED_ORIGINS",
    default=["https://__DOMAIN__", "https://www.__DOMAIN__"],
)
# CORS_ALLOWED_ORIGINS — browser origins allowed to call /api cross-origin
# (scoped by CORS_URLS_REGEX="^/api/.*$" in base.py). Closed by default: empty
# means same-origin only. Add full origins WITH scheme via env, e.g.
# DJANGO_CORS_ALLOWED_ORIGINS=https://app.example.com,https://admin.example.com
# https://github.com/adamchainz/django-cors-headers#cors_allowed_origins-sequencestr
CORS_ALLOWED_ORIGINS = env.list("DJANGO_CORS_ALLOWED_ORIGINS", default=[])


